"""Tests for AudioTagger - Picard tag read/write mapping across FLAC/MP3/M4A.

Reads the committed fixtures in ``tests/fixtures/library/`` (generated by
``generate.py`` with raw mutagen - an independent oracle). Write/roundtrip tests
copy a fixture to a tmp path so the committed files stay pristine.
"""

import shutil
from pathlib import Path

import pytest

from infrastructure.audio.tagger import AudioTagger
from models.audio import AudioTag

FIXTURES = Path(__file__).resolve().parent.parent / "fixtures" / "library"

# A tiny but valid PNG-ish blob - enough to roundtrip as cover-art bytes.
_COVER = b"\x89PNG\r\n\x1a\n" + b"\x00" * 96


@pytest.fixture
def tagger() -> AudioTagger:
    return AudioTagger()


@pytest.fixture
def tmp_copy(tmp_path: Path):
    def _copy(name: str) -> Path:
        dest = tmp_path / name
        shutil.copy(FIXTURES / name, dest)
        return dest

    return _copy


