# -- multi-value genre: read surfaces every value; import write never flattens --


def _set_genres(path: Path, genres: list[str]) -> None:
    """Write multiple genre values with raw mutagen (an independent oracle)."""
    import mutagen
    from mutagen.id3 import TCON
    from mutagen.mp3 import MP3
    from mutagen.mp4 import MP4

    audio = mutagen.File(path)
    if isinstance(audio, MP4):
        audio.tags["\xa9gen"] = genres
    elif isinstance(audio, MP3):
        audio.tags.setall("TCON", [TCON(encoding=3, text=genres)])
    else:
        audio["GENRE"] = genres
    audio.save()


@pytest.mark.parametrize("name", ["flac_full_01.flac", "mp3_full_01.mp3", "m4a_full_01.m4a"])
def test_read_multi_genre_surfaces_all_values(tagger, tmp_copy, name):
    path = tmp_copy(name)
    _set_genres(path, ["Rock", "Jazz", "Electronic"])
    tag, _ = tagger.read_tags(path)
    assert tag.genre == "Rock; Jazz; Electronic"


@pytest.mark.parametrize("name", ["flac_full_01.flac", "mp3_full_01.mp3", "m4a_full_01.m4a"])
def test_write_album_identity_preserves_descriptive_tags(tagger, tmp_copy, name):
    """The import write stamps the album identity but must leave the file's own
    title/artist and (multi-value) genre exactly as they were."""
    import msgspec

    path = tmp_copy(name)
    _set_genres(path, ["Rock", "Jazz"])
    before, _ = tagger.read_tags(path)
    new = msgspec.structs.replace(
        before,
        album="New Album",
        album_artist="New AA",
        year=2021,
        musicbrainz_release_group_id="rg-owned",
    )
    tagger.write_album_identity(path, new)
    after, _ = tagger.read_tags(path)

    # owned album identity updated...
    assert after.album == "New Album"
    assert after.album_artist == "New AA"
    assert after.year == 2021
    assert after.musicbrainz_release_group_id == "rg-owned"
    # ...while the file's own descriptive tags are left untouched - crucially the
    # two genres survive instead of being flattened to "Rock".
    assert after.title == before.title
    assert after.artist == before.artist
    assert after.genre == "Rock; Jazz"
