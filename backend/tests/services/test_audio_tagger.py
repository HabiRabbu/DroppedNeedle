# -- read: edge cases --


def test_mp3_recording_id_is_stored_as_ufid_not_release_track(tagger, tmp_copy):
    # The recording MBID must live in the UFID frame (Picard), never the
    # "Release Track Id" TXXX — that's a different MusicBrainz entity.
    import mutagen

    raw = mutagen.File(tmp_copy("mp3_full_01.mp3"))
    assert "UFID:http://musicbrainz.org" in raw.tags
    assert "TXXX:MusicBrainz Release Track Id" not in raw.tags


def test_only_release_mbid_leaves_release_group_none(tagger):
    tag, _ = tagger.read_tags(FIXTURES / "flac_only_release_mbid.flac")
    assert tag.musicbrainz_release_group_id is None
    assert tag.musicbrainz_release_id == "rel-only-0001"


def test_cjk_tags_preserved(tagger):
    tag, _ = tagger.read_tags(FIXTURES / "flac_cjk_01.flac")
    assert tag.artist == "ユキ"
    assert tag.title == "桃源へ"


def test_no_tags_yields_empty_strings(tagger):
    tag, _ = tagger.read_tags(FIXTURES / "flac_no_tags.flac")
    assert tag.title == ""
    assert tag.artist == ""
    assert tag.musicbrainz_release_group_id is None


def test_unreadable_file_raises(tagger, tmp_path):
    junk = tmp_path / "not-audio.flac"
    junk.write_bytes(b"this is not audio")
    with pytest.raises(ValueError):
        tagger.read_tags(junk)


# -- write: roundtrip per format --


@pytest.mark.parametrize("name", ["flac_full_01.flac", "mp3_full_01.mp3", "m4a_full_01.m4a"])
def test_write_mb_tags_roundtrip(tagger, tmp_copy, name):
    path = tmp_copy(name)
    new = AudioTag(
        title="New Title",
        artist="New Artist",
        album="New Album",
        album_artist="New AA",
        track_number=7,
        disc_number=2,
        year=2020,
        genre="Jazz",
        musicbrainz_release_group_id="rg-new",
        musicbrainz_release_id="rel-new",
        musicbrainz_recording_id="rec-new",
        musicbrainz_artist_id="art-new",
        musicbrainz_album_artist_id="aart-new",
        acoustid_id="ac-new",
        compilation=True,
    )
    tagger.write_mb_tags(path, new)
    back, _ = tagger.read_tags(path)
    assert back.title == "New Title"
    assert back.track_number == 7
    assert back.disc_number == 2
    assert back.year == 2020
    assert back.genre == "Jazz"
    assert back.compilation is True
    assert back.musicbrainz_release_group_id == "rg-new"
    assert back.musicbrainz_recording_id == "rec-new"
    assert back.musicbrainz_artist_id == "art-new"
    assert back.musicbrainz_album_artist_id == "aart-new"
    assert back.acoustid_id == "ac-new"


@pytest.mark.parametrize("name", ["flac_full_01.flac", "mp3_full_01.mp3", "m4a_full_01.m4a"])
def test_cover_art_roundtrip(tagger, tmp_copy, name):
    path = tmp_copy(name)
    tagger.write_cover_art(path, _COVER)
    assert tagger.read_cover_art(path) == _COVER
